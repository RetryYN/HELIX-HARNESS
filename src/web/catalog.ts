type ScreenTuple = [
  id: string,
  name: string,
  category: ScreenSpec["category"],
  url: string,
  l1Reference: string,
  specificComponents: string[],
  commonComponents: string[],
  mission: string,
  primaryStatus: StatusLevel,
];

function screen(...input: ScreenTuple): ScreenSpec {
  const [
    id,
    name,
    category,
    url,
    l1Reference,
    specificComponents,
    commonComponents,
    mission,
    primaryStatus,
  ] = input;
  return {
    id,
    name,
    category,
    url,
    l1Reference,
    specificComponents,
    commonComponents,
    mission,
    primaryStatus,
  };
}

export function allComponents(): UiComponentSpec[] {
  return [...COMMON_COMPONENTS, ...SPECIFIC_COMPONENTS];
}

export function componentNames(): Set<string> {
  return new Set(allComponents().map((component) => component.name));
}

export function findScreen(id: string): ScreenSpec | null {
  return SCREEN_SPECS.find((screen) => screen.id === id) ?? null;
}
